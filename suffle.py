def address():
    updated_matched_data = []
    with open('WALLET01.csv', newline='') as csvfile:
        reader = csv.reader(csvfile)
        next(reader, None)  # 헤더 스킵
        for row in reader:
            updated_matched_data.append(row)

        with open('updated_matched_data.csv', 'w', newline='') as file:
            writer = csv.writer(file)
            writer.writerow(['Address', 'Value'])
            for data in updated_matched_data:
                writer.writerow(data)

    for match in updated_matched_data:
        key = int(match[1])  # 2열 데이터
        value = match[0]  # 1열 데이터
        values = ('"'+ value + '",\n') * key  # 2열의 회수만큼 1열을 곱함
        with open(f'address/{value}_values.txt', 'w') as file:
            file.write(values)
        
